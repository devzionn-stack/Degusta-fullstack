export async function popularCardapio(tenantId: string) {
  console.log(`üçï Populando card√°pio para tenant ${tenantId}...`);

  for (const pizza of cardapio) {
    try {
      await db.insert(produtos).values({
        tenantId,
        ...pizza,
      });
      console.log(`‚úÖ ${pizza.nome} cadastrada`);
    } catch (error) {
      console.error(`‚ùå Erro ao cadastrar ${pizza.nome}:`, error);
    }
  }

  console.log(`\nüéâ Card√°pio completo! ${cardapio.length} pizzas cadastradas.\n`);
}

